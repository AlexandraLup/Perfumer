<p>3. Definiții
  <br>
  <p>
Contract de vânzare - orice contract în temeiul căruia vânzătorul transferă sau se angajează să transfere proprietatea asupra unor produse către consumator, iar consumatorul plătește sau se angajează să plătească prețul acestora.
<p>Vânzătorul (distribuitorul) - o persoană, care în concordanță cu termenii și condițiile contractuale oferă produsul  consumatorului. Este orice persoană fizică sau juridică, publică sau privată, care acționează în cadrul activității sale comerciale, industriale sau de producție, artizanale ori liberale în legătura cu contractele care intră sub incidenta OUG nr. 34/2014, precum și orice persoană care acționează în același scop, în numele său pe seama acesteia.
 <p>Consumator (cumpărătorul) - orice persoană fizică sau grup de persoane fizice constituite în asociații, care în concordanță cu termenii și condițiile contractuale, acţionează în scopuri din afara activității sale comerciale, industriale sau de producție, artizanale ori liberale.
 <p>Distribuitor (operatorul economic din lanțul de distribuție) - o persoană fizică sau juridică, autorizată, care, în cadrul activității sale profesionale, în concordanță cu termenii și condițiile contractuale, fabrică, importă, transportă sau comercializează produse ori părți din acestea sau prestează servicii.
 <p>Încheierea contractului de vânzare - comanda facută de cumpărător este un act cu efecte juridice, sub condiția confirmării și acceptării comenzii de către vânzător. Din momentul acceptării comenzii de către vânzător  și recepționării acceptării comenzii de către cumpărător între părți este încheiat un contract generator de drepturi și obligații în sarcina ambelor părti, asa cum sunt definite ele în contract sau în termenii și condițiile prezente, care sunt parte integrantă din acest contract.
 <p>Termeni și condiții - înțelegerea contractuală dintre vânzător și cumpărător, în conformitate cu termenii și condițiile arătate în această secțiune. Cumpărătorul acceptă termenii în care este încheiat contractul la momentul transmiterii comenzii, comanda care generează efecte juridice de la momentul încheierii contractului de vânzare prin confirmarea și acceptarea comenzii de către vânzător. Cumpărătorul se obligă sa ia la cunoștință prezentele condiții, condiții pe care se consideră ca le-a acceptat din momentul transmiterii comenzii către vânzător.

